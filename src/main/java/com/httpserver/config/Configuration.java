package com.httpserver.config;

public class Configuration {
    private String webroute;
    private int port;

    public String getWebroute() {
        return webroute;
    }

    public void setWebroute(String webroute) {
        this.webroute = webroute;
    }

    public int getPort() {
        return port;
    }

    public void setPort(int port) {
        this.port = port;
    }
}
